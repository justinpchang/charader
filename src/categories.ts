export const getRandomOptionsForCategory = (
  categoryId: string,
  count: number
): string[] => {
  if (categoryId === "misc") {
    // For misc, get random options from all categories
    const allOptions = Object.values(CATEGORY_OPTIONS).flatMap(
      (cat) => cat.options
    );
    return shuffleArray([...allOptions]).slice(0, count);
  }

  if (categoryId === "custom") {
    return [];
  }

  const categoryOptions = CATEGORY_OPTIONS[categoryId]?.options || [];
  return shuffleArray([...categoryOptions]).slice(0, count);
};

const shuffleArray = <T>(array: T[]): T[] => {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
};
